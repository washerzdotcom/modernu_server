import categoryModel from "../models/categoryModel.js";
import slugify from "slugify";

export const createCategoryController = async (req, res) => {
  try {
    const { name } = req.body;
    const { productType } = req.query;
    if (!name) {
      return res.status(401).send({ message: "Name is requred" });
    }
    if (!productType) {
      return res.status(401).send({ message: "productType is requred" });
    }
    // const existingCategory = await categoryModel.findOne({ name, productType });
    // if (existingCategory) {
    //   return res.status(200).send({
    //     success: true,
    //     message: "Category Already Exisits",
    //   });
    // }
    const category = await new categoryModel({
      name,
      slug: slugify(name),
      productType,
    }).save();
    res.status(201).send({
      success: true,
      message: "new category created",
      category,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error in Category",
    });
  }
};

//update category
export const updateCategoryController = async (req, res) => {
  try {
    const { name } = req.body;
    const { id } = req.params;
    const category = await categoryModel.findByIdAndUpdate(
      id,
      { name, slug: slugify(name) },
      { new: true }
    );
    res.status(200).send({
      success: true,
      message: "Catefory Updated Successfully",
      category,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error while updating category",
    });
  }
};

// get all category
export const categoryController = async (req, res) => {
  try {
    const { productType } = req.query;
    const category = await categoryModel.find({ productType: productType });
    res.status(200).send({
      success: true,
      message: "All Categories List",
      category,
    });
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      error,
      message: "Error while getting all categories",
    });
  }
};

